// Java Star Pattern part 2
class JavaStarPattern {
    public static void main(String[] args) {
        int i,j;
        for(i=1;i<=20;i++)
        {
           for(j=1;j<=20;j++)
           {
                 System.out.print("*");
           }
           System.out.print("\n");
        }
    }
    
}
